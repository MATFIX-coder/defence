function escapeHtml(text) {
  return String(text)
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#039;');
}

function showToast(text = 'Скопировано') {
  toast.textContent = text;
  toast.classList.add('show');
  setTimeout(() => toast.classList.remove('show'), 1300);
}

async function copyText(text) {
  try {
    await navigator.clipboard.writeText(text);
    showToast();
  } catch (e) {
    const area = document.createElement('textarea');
    area.value = text;
    document.body.appendChild(area);
    area.select();
    document.execCommand('copy');
    area.remove();
    showToast();
  }
}

function setHeroImage(sectionId) {
  const section = DATA.find(item => item.id === sectionId);
  if (!section || !heroImage) return;

  heroImage.src = section.headerImage || 'assets/header.png?v=20260529-9';
  heroImage.alt = section.title;
}


function makeSolutionCode(task, number) {
  const code = String(task.code || '');

  if (code.trimStart().startsWith('#')) {
    return code;
  }

  const textLines = String(task.text || '')
    .replaceAll('\r', '')
    .split('\n')
    .flatMap(line => {
      const clean = line.trim();
      if (clean.length <= 86) return [clean];

      const parts = [];
      let rest = clean;
      while (rest.length > 86) {
        let cut = rest.lastIndexOf(' ', 86);
        if (cut <= 0) cut = 86;
        parts.push(rest.slice(0, cut));
        rest = rest.slice(cut).trimStart();
      }
      if (rest) parts.push(rest);
      return parts;
    });

  const header = [
    `# ${number}. ${task.title}`,
    ...textLines.map(line => `# ${line}`),
    `# ${task.answer}`,
    ''
  ].join('\n');

  return header + code;
}

function makeLockedBlock(title) {
  return `
    <div class="block locked-block">
      <div class="block-head"><strong>${title}</strong></div>
      <div class="locked-body">
        Доступ закрыт. Введите код сотрудника, чтобы открыть ответы и решения.
      </div>
    </div>
  `;
}

function makeBlock(kind, title, content, copy = false) {
  const cls = kind === 'answer' ? 'block answer' : kind === 'solution' ? 'block solution' : 'block data-block';
  const escaped = kind === 'answer' ? escapeHtml(content) : escapeHtml(content);
  const copyBtn = copy ? `<button class="copy-btn" data-copy="${escapeHtml(content)}">Копировать</button>` : '';

  if (kind === 'answer') {
    return `<div class="${cls}"><div class="block-head"><strong>${title}</strong></div><div class="answer-body">${escaped}</div></div>`;
  }

  return `<div class="${cls}"><div class="block-head"><strong>${title}</strong>${copyBtn}</div><pre><code>${escaped}</code></pre></div>`;
}

function render() {
  sectionsRoot.innerHTML = '';
  tabsRoot.innerHTML = '';
  updateAccessUi();
  const total = DATA.reduce((sum, s) => sum + s.groups.reduce((a, g) => a + g.tasks.length, 0), 0);
  document.getElementById('totalTasks').textContent = `${total} задач`;

  DATA.forEach((section, index) => {
    const tab = document.createElement('button');
    tab.className = `tab ${index === 0 ? 'active' : ''}`;
    tab.dataset.target = section.id;
    tab.textContent = section.title.replace(' – ', ': ');
    tabsRoot.appendChild(tab);

    const sec = document.createElement('section');
    sec.id = section.id;
    sec.className = `section ${index === 0 ? 'active' : ''}`;
    sec.innerHTML = `
      <div class="section-header">
        ${section.heroImage ? `<div class="section-hero-image-wrap"><img src="${escapeHtml(section.heroImage)}" alt="${escapeHtml(section.title)}" class="section-hero-image" /></div>` : ''}
        <h2>${escapeHtml(section.title)}</h2>
        <p>${escapeHtml(section.desc)}</p>
      </div>
    `;

    section.groups.forEach(group => {
      const h = document.createElement('h3');
      h.className = 'subsection-title';
      h.textContent = group.title;
      sec.appendChild(h);

      const grid = document.createElement('div');
      grid.className = 'grid';

      group.tasks.forEach((task, i) => {
        const card = document.createElement('article');
        card.className = 'card';
        card.dataset.search = `${section.title} ${group.title} ${task.title} ${task.text} ${task.answer} ${(task.tags || []).join(' ')}`.toLowerCase();
        const tags = (task.tags || []).map(t => `<span class="tag">${escapeHtml(t)}</span>`).join('');
        const solutionCode = makeSolutionCode(task, i + 1);
        const protectedContent = teacherMode ? `
          ${makeBlock('answer', 'Ответ', task.answer, false)}
          <div class="card-actions">
            <button class="small-btn solution-toggle">Показать решение</button>
            <button class="small-btn copy-code" data-copy="${escapeHtml(solutionCode)}">Копировать решение</button>
          </div>
          ${makeBlock('solution', 'Решение', solutionCode, false)}
        ` : `
          ${makeLockedBlock('Ответ')}
          ${makeLockedBlock('Решение')}
        `;

        card.innerHTML = `
          <div class="card-head">
            <div class="num">${i + 1}</div>
            <div>
              <h3>${escapeHtml(task.title)}</h3>
              <p>${escapeHtml(task.text)}</p>
              ${tags ? `<div class="tag-row">${tags}</div>` : ''}
            </div>
          </div>
          ${task.data ? makeBlock('data', 'Дано для копирования в Python', task.data, true) : ''}
          ${protectedContent}
        `;
        grid.appendChild(card);
      });
      sec.appendChild(grid);
    });

    const empty = document.createElement('div');
    empty.className = 'empty';
    empty.textContent = 'По этому запросу задач не найдено.';
    sec.appendChild(empty);
    sectionsRoot.appendChild(sec);
  });
}

function updateAccessUi() {
  document.body.classList.toggle('teacher-mode', teacherMode);
  document.body.classList.toggle('student-mode', !teacherMode);

  if (accessStatus) {
    accessStatus.textContent = teacherMode ? 'режим сотрудника' : 'режим ученика';
    accessStatus.classList.toggle('access-open', teacherMode);
  }

  [document.getElementById('toggleAnswersBtn'), document.getElementById('openAllBtn'), document.getElementById('closeAllBtn')]
    .forEach(button => {
      if (!button) return;
      button.disabled = !teacherMode;
      button.classList.toggle('disabled-control', !teacherMode);
    });

  if (lockBtn) lockBtn.style.display = teacherMode ? 'inline-flex' : 'none';
}

function unlockTeacherMode() {
  const value = (employeeCodeInput?.value || '').trim().toUpperCase();

  if (value !== STAFF_CODE) {
    showToast('Неверный код');
    if (employeeCodeInput) employeeCodeInput.value = '';
    return;
  }

  teacherMode = true;
  localStorage.setItem('pythonDefenseTeacherMode', '1');
  answersHidden = false;
  document.body.classList.remove('answers-hidden');
  showToast('Ответы открыты');
  render();
}

function lockTeacherMode() {
  teacherMode = false;
  localStorage.removeItem('pythonDefenseTeacherMode');
  answersHidden = false;
  document.body.classList.remove('answers-hidden');
  if (employeeCodeInput) employeeCodeInput.value = '';
  showToast('Режим ученика');
  render();
}

function bindEvents() {
  tabsRoot.addEventListener('click', (e) => {
    const tab = e.target.closest('.tab');
    if (!tab) return;
    document.querySelectorAll('.tab').forEach(t => t.classList.remove('active'));
    document.querySelectorAll('.section').forEach(s => s.classList.remove('active'));
    tab.classList.add('active');
    document.getElementById(tab.dataset.target).classList.add('active');
    setHeroImage(tab.dataset.target);
  });

  document.body.addEventListener('click', (e) => {
    const copy = e.target.closest('[data-copy]');
    if (copy) {
      if (copy.classList.contains('copy-code') && !teacherMode) {
        showToast('Введите код сотрудника');
        return;
      }
      copyText(copy.dataset.copy);
    }

    const toggle = e.target.closest('.solution-toggle');
    if (toggle) {
      if (!teacherMode) {
        showToast('Введите код сотрудника');
        return;
      }
      const card = toggle.closest('.card');
      const solution = card.querySelector('.solution');
      solution.classList.toggle('open');
      toggle.textContent = solution.classList.contains('open') ? 'Скрыть решение' : 'Показать решение';
    }
  });

  document.getElementById('toggleAnswersBtn').addEventListener('click', () => {
    if (!teacherMode) {
      showToast('Введите код сотрудника');
      return;
    }
    answersHidden = !answersHidden;
    document.body.classList.toggle('answers-hidden', answersHidden);
    document.getElementById('toggleAnswersBtn').textContent = answersHidden ? 'Показать ответы' : 'Скрыть ответы';
  });

  document.getElementById('openAllBtn').addEventListener('click', () => {
    if (!teacherMode) {
      showToast('Введите код сотрудника');
      return;
    }
    document.querySelectorAll('.solution').forEach(s => s.classList.add('open'));
    document.querySelectorAll('.solution-toggle').forEach(b => b.textContent = 'Скрыть решение');
  });

  document.getElementById('closeAllBtn').addEventListener('click', () => {
    if (!teacherMode) {
      showToast('Введите код сотрудника');
      return;
    }
    document.querySelectorAll('.solution').forEach(s => s.classList.remove('open'));
    document.querySelectorAll('.solution-toggle').forEach(b => b.textContent = 'Показать решение');
  });

  if (unlockBtn) unlockBtn.addEventListener('click', unlockTeacherMode);
  if (lockBtn) lockBtn.addEventListener('click', lockTeacherMode);
  if (employeeCodeInput) {
    employeeCodeInput.addEventListener('keydown', (e) => {
      if (e.key === 'Enter') unlockTeacherMode();
    });
  }

  searchInput.addEventListener('input', () => {
    const q = searchInput.value.trim().toLowerCase();
    document.querySelectorAll('.section').forEach(section => {
      let visible = 0;
      section.querySelectorAll('.card').forEach(card => {
        const hit = !q || card.dataset.search.includes(q);
        card.classList.toggle('hidden-by-search', !hit);
        if (hit) visible++;
      });
      const empty = section.querySelector('.empty');
      if (empty) empty.style.display = visible ? 'none' : 'block';
    });
  });
}

render();
setHeroImage(DATA[0].id);
bindEvents();
